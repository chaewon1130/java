package comp.pcwk.ed03;
public class EX03Operator {
	public static void main(String[] args) {
		/*
		 논리 부정 연산자( ! )
		 참을 거짓으로 거짓을 참으로 바꾸는 연산자
		*/
		
		boolean a = true;
		System.out.println("a = "+a);
		System.out.println("!a = "+!a);
		
		boolean b = false;
		System.out.println("b = "+b);
		System.out.println("!b = "+!b);
	}
}